/* ===== Конфигурация сборки «Сенсор Suite» =====
   Подключается ДО app.js. Позволяет собирать пер-отдельные версии без серверов.

   enabledModules:
     null            — показать ВСЕ зарегистрированные модули (полный стенд, по умолчанию);
     ['documents',…] — белый список id модулей (плюс системные: 'settings' всегда доступен).

   Можно переопределить из URL: ?dept=documents  или  ?dept=documents,licensing
   (удобно для демо-ссылок на конкретный отдел без отдельной сборки).

   Прочее — необязательные брендовые подписи пер-отдел. */
window.SUITE_CONFIG = window.SUITE_CONFIG || {
  enabledModules: null,
  // подпись под логотипом; если null — берётся дефолт из index.html
  brandSubtitle: null,
  // принудительная тема при первом запуске ('light' | 'dark' | null — не навязывать)
  forceTheme: null
};

/* ===== Роли (режимы доступа) =====
   Карта роль → список id модулей. Применяется В ПЕРЕСЕЧЕНИИ с enabledModules/?dept
   (см. allowedIds() в app.js): модуль виден, только если разрешён И ролью, И сборкой.
   'settings' добавляется к любой роли автоматически (системный модуль).

     manager  — руководитель: все модули (null = без ограничений роли);
     operator — оператор: операционные модули без руководительской аналитики
                (РНП-дашборд management и конкурентная аналитика analytics скрыты).

   Дефолтная роль и порядок переключателя заданы ниже. Меняется в Настройках
   (сегмент-контрол) или из палитры; смена мгновенно перестраивает навигацию. */
window.SUITE_ROLES = window.SUITE_ROLES || {
  defaultRole: 'manager',
  order: ['manager', 'operator'],
  labels: { manager: 'Руководитель', operator: 'Оператор' },
  hints:  {
    manager:  'Полный доступ: все модули, включая РНП-дашборд и аналитику.',
    operator: 'Операционный доступ: документы, лицензирование, специалисты и т.д. Без руководительской аналитики.'
  },
  // null = все модули; массив = белый список id (плюс 'settings' всегда)
  modules: {
    manager:  null,
    operator: ['documents', 'licensing', 'specialists', 'nok', 'sales', 'counterparties', 'registry', 'settings']
  }
};
