/* ============================================================================
   js/data/objections-tags.js — слой тегов поверх банка возражений.
   Глобальный объект window.SensorObjectionTags (без import/export — классический
   <script>, как и весь js/). Подключается ПОСЛЕ js/data/content.js, потому что
   опирается на window.SEED.objections.bank[].cat как на единственный источник
   списка категорий (этот файл НЕ дублирует ответы и НЕ переопределяет банк).

   Зачем: банк возражений (content.js) — плоский список из 14 категорий без
   привязки к этапу продажи и продукту. Этот слой добавляет навигацию «по этапу /
   по продукту», чтобы менеджер на конкретном шаге звонка (и по конкретной услуге)
   быстро находил релевантные возражения. Интеграция фильтра в UI sales.js
   (вкладка «Возражения») — отдельной поздней волной; здесь только данные + чистые
   функции, без DOM.

   Этапы (stages) — канонический словарь воронки, к которому сводятся шаги
   скриптов МЧС/АТТПР из content.js (Представиться/Программирование → «Открытие»;
   Выявление потребности/Квалификация → «Выявление потребности»; Презентация/
   Сроки/Стоимость → «Презентация»; Предзакрытие/Закрытие/Отработка → «Закрытие»):
     • 'Открытие'                — установление контакта, дозвон, секретарь/ЛПР.
     • 'Выявление потребности'   — квалификация, боли, бюджет, кто решает.
     • 'Презентация'             — аргументация ценности, сроки, стоимость.
     • 'Закрытие'                — дожим, отработка финальных сомнений, оплата.

   Продукты (products) — линейка Sensor:
     • 'МЧС'   — лицензия МЧС (монтаж/ТО/ремонт систем ПБ).
     • 'АТТПР' — аттестация проектировщика.
     • 'НОК'   — независимая оценка квалификации.

   Категории берутся РОВНО как в content.js bank[].cat (14 шт). Если категория в
   банке переименуется/добавится — синхронизировать TAGS ниже; test/objections-tags.mjs
   проверяет, что покрыты все известные cat и нет ссылок на несуществующие.
   ========================================================================== */
(function () {
  'use strict';

  var STAGES   = ['Открытие', 'Выявление потребности', 'Презентация', 'Закрытие'];
  var PRODUCTS = ['МЧС', 'АТТПР', 'НОК'];

  /* Карта тегов: ключ — строка cat из window.SEED.objections.bank, значение —
     { stages:[...], products:[...] }. Возражение может относиться к нескольким
     этапам и продуктам. Все три продукта Sensor продаются по одному скрипту,
     поэтому большинство возражений общие для МЧС/АТТПР/НОК; продукт-специфика
     отмечена точечно (например, «Пока нет заказов» / «Сезон» — про монтажный
     рынок лицензии МЧС больше, чем про аттестацию физлица). */
  var TAGS = {
    'Дорого (упущенная выгода / экономия / штрафы)': {
      stages:   ['Презентация', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Нет бюджета / нет денег': {
      stages:   ['Выявление потребности', 'Презентация', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Решает директор': {
      stages:   ['Выявление потребности', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Пока нет заказов': {
      stages:   ['Выявление потребности', 'Презентация'],
      products: ['МЧС', 'АТТПР']
    },
    'Отправляйте всё на почту': {
      stages:   ['Открытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Есть поставщик рядом': {
      stages:   ['Выявление потребности', 'Презентация'],
      products: ['МЧС', 'АТТПР']
    },
    'Я сам вам перезвоню': {
      stages:   ['Открытие', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Пока всё заморозили': {
      stages:   ['Выявление потребности', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Ничего не нужно': {
      stages:   ['Открытие', 'Выявление потребности'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Сезон / не сезон': {
      stages:   ['Выявление потребности', 'Презентация'],
      products: ['МЧС', 'АТТПР']
    },
    'Не интересно / Ещё не смотрел / Я подумаю': {
      stages:   ['Презентация', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Нет времени / Имейте в виду / Мы подумаем': {
      stages:   ['Открытие', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Конкуренты и текущие поставщики': {
      stages:   ['Выявление потребности', 'Презентация'],
      products: ['МЧС', 'АТТПР', 'НОК']
    },
    'Прочее': {
      stages:   ['Презентация', 'Закрытие'],
      products: ['МЧС', 'АТТПР', 'НОК']
    }
  };

  /* Известные категории банка (для согласованности фильтров). Если банк доступен
     на момент загрузки — берём cat из него; иначе fallback к ключам TAGS. */
  function bankCats() {
    var bank = (window.SEED && window.SEED.objections && window.SEED.objections.bank) || [];
    if (bank.length) {
      return bank.map(function (b) { return b && b.cat; }).filter(Boolean);
    }
    return Object.keys(TAGS);
  }

  /* Все категории, помеченные данным этапом. Возвращает подмножество известных
     cat (порядок — как в банке). */
  function byStage(stage) {
    var cats = bankCats();
    return cats.filter(function (cat) {
      var t = TAGS[cat];
      return !!(t && t.stages && t.stages.indexOf(stage) >= 0);
    });
  }

  /* Все категории, помеченные данным продуктом. */
  function byProduct(product) {
    var cats = bankCats();
    return cats.filter(function (cat) {
      var t = TAGS[cat];
      return !!(t && t.products && t.products.indexOf(product) >= 0);
    });
  }

  /* Полная карта тегов (копия, чтобы потребитель не мутировал исходник). */
  function all() {
    var out = {};
    Object.keys(TAGS).forEach(function (cat) {
      out[cat] = {
        stages:   TAGS[cat].stages.slice(),
        products: TAGS[cat].products.slice()
      };
    });
    return out;
  }

  window.SensorObjectionTags = {
    stages:   STAGES.slice(),
    products: PRODUCTS.slice(),
    tags:     TAGS,
    byStage:  byStage,
    byProduct: byProduct,
    all:      all
  };
})();
