    function verify(kind, edu, prof, exp, mchsExp){
      const reasons = [];
      let ok = true, note = '';

      // 1. Базовое образование: школа не подходит ни для чего.
      if(edu==='school'){
        reasons.push({ok:false, text:'Образование — только школа. Нужно не ниже СПО (среднее профессиональное) или высшее.'});
        ok = false;
      } else {
        reasons.push({ok:true, text:'Образование '+(edu==='higher'?'высшее':'СПО')+' — соответствует минимальному уровню.'});
      }

      // 2. Профильность.
      if(prof==='fire'){
        reasons.push({ok:true, text:'Профиль «Пожарная / Техносферная безопасность» — профпереподготовка не требуется.'});
      } else if(prof==='retrain'){
        if(edu==='school'){
          reasons.push({ok:false, text:'Профпереподготовка засчитывается только поверх СПО/высшего — у специалиста его нет.'});
        } else {
          reasons.push({ok:true, text:'Непрофильное образование закрыто профпереподготовкой ≥250 ч (программу даёт наш УЦ).'});
        }
      } else { // none
        reasons.push({ok:false, text:'Образование непрофильное и без профпереподготовки. Нужен профиль ПБ либо профпереподготовка ≥250 ч.'});
        ok = false;
      }

      if(kind==='mchs'){
        // 3. Стаж ≥5 лет.
        if(exp>=5){
          reasons.push({ok:true, text:'Стаж '+exp+' лет — выполняет требование ≥5 лет для ответственного за ПБ.'});
        } else {
          reasons.push({ok:false, text:'Стаж '+exp+' лет — недостаточно, нужно ≥5 лет по профилю.'});
          ok = false;
        }
        // 4. Опыт под лицензией МЧС / в Госпожнадзоре.
        if(mchsExp){
          reasons.push({ok:true, text:'Есть опыт в компании с лицензией МЧС / в Госпожнадзоре — стаж засчитывается корректно.'});
        } else {
          reasons.push({ok:false, text:'Нет опыта под лицензией МЧС / в Госпожнадзоре — стаж по ПБ может не зачесться.'});
          ok = false;
        }
        note = 'Ответственный за ПБ может быть оформлен только у одного юрлица/ИП. Допускается совместительство по трудовому договору.';
      } else { // attpr
        if(exp>=1){
          reasons.push({ok:true, text:'Опыт по профилю '+exp+' лет — для аттестации проектировщика жёсткого ценза по стажу нет, профильная подготовка важнее.'});
        } else {
          reasons.push({ok:true, text:'Стаж по профилю не критичен для АТТПР — экзамен проверяет знания, а не выслугу.'});
        }
        note = 'Аттестацию принимает ГУ МЧС по центру федерального округа по прописке проектировщика. При непрофильном образовании сначала профпереподготовка, затем аттестация.';
      }

      return { ok, reasons, note };
    }

